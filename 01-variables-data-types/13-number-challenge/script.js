let x = Math.floor(Math.random() * (100 - 1 + 1) + 1);
let y = Math.floor(Math.random() * (50 - 1 + 1) + 1);

x = Math.floor(x);
console.log('x is equal to: ' + x);
y = Math.floor(y);
console.log('y is equal to: ' + y);

console.log('x + y is equal to: ' + (x + y));
console.log('x - y is equal to: ' + (x - y));
console.log('x * y is equal to: ' + (x * y));
console.log('x / y is equal to: ' + (x / y));
